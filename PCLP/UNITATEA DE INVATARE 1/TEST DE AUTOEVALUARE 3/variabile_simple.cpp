#include <iostream>

int main() {
    int varInt = 5;       // Variabilă de tip int
    double varDouble = 3.14;  // Variabilă de tip double
    char varChar = 'A';    // Variabilă de tip char
    bool varBool = true;   // Variabilă de tip bool

    std::cout << "Valoarea lui varInt: " << varInt << std::endl;
    std::cout << "Valoarea lui varDouble: " << varDouble << std::endl;
    std::cout << "Valoarea lui varChar: " << varChar << std::endl;
    std::cout << "Valoarea lui varBool: " << varBool << std::endl;
    system("pause");
    return 0;
}
